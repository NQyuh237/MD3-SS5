package BT;

public class BT1 {
    public static void main(String[] args) {
        String myString = "Hello, World!";

        if (myString.isEmpty()) {
            System.out.println("Chuỗi rỗng.");
        } else {
            System.out.println("Chuỗi không rỗng.");
        }

        String emptyString = "";

        if (emptyString.isEmpty()) {
            System.out.println("Chuỗi rỗng.");
        } else {
            System.out.println("Chuỗi không rỗng.");
        }
    }
}
